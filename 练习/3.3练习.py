#3.3.1 列表排序
cars= ['bmw','audi','toyota','subaru']
cars.sort()
print(cars)
cars.sort(reverse=True)
print(cars)

#3.3.2临时排序
cars= ['bmw','audi','toyota','subaru']
print("Here is the original list:")
print (cars)
print("\nHere is the sorted list:" )
print(sorted(cars))
print("\nHere is the original list:")
print (cars)

#3.3.3 倒着打印列表
cars= ['bmw','audi','toyota','subaru']
cars.reverse()
print(cars)
cars.reverse()
print(cars)

#3.3.4 确定列表的长度
cars= ['bmw','audi','toyota','subaru']
print(len(cars))

